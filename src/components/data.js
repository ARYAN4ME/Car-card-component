  // Function to generate a random price between $300 and $500
   export function getRandomPrice() {
    return Math.floor(Math.random() * (500 - 300 + 1)) + 300;
  }

  function getRandomDistance() {
    return Math.floor(Math.random() * (10 - 5 + 1)) + 5;
  }
  // Array of possible brand values
const brands = ["Audi", "BMW", "Toyota", "Volvo"];

function getRandomBrand() {
  const randomIndex = Math.floor(Math.random() * brands.length);
  return brands[randomIndex];
}
const relevance = ["Petrol", "Fuels", "Hybrid", "Gasoline"];
// Function to get a random brand from the brands array

function getRandomRelevance() {
    const randomIndex = Math.floor(Math.random() * relevance.length);
    return relevance[randomIndex];
  }
  
  
  // Loop through each object and add the "price" key with a random value
  for (let i = 0; i < data.length; i++) {
    data[i].price = `$${getRandomPrice()}`;
    data[i].distance = `${getRandomDistance()} km`;
    data[i].brand = getRandomBrand();
    data[i].relevance = getRandomRelevance();
  }
  
  

  